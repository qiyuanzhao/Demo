package boot.pojo;


public class User {
    private String displayName;
    private String site;

    public String getDisplayName() {
        return displayName;
    }

    public String getSite() {
        return site;
    }

    public void setDisplayName(String displayName) {
        this.displayName = displayName;
    }

    public void setSite(String site) {
        this.site = site;
    }
}
